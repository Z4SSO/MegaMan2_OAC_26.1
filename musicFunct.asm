.text
MUSIC_LOOP:
    addi sp, sp, -4
    sw ra, 0(sp)

    la a0, CANAL_1
    li a2, INSTRUMENTO1
    li a3, VOLUME1
    call PLAY_MUSIC
    mv  t5, a7            # guarda se CANAL_1 terminou

    la a0, CANAL_2
    li a2, INSTRUMENTO2
    li a3, VOLUME2
    call PLAY_MUSIC
    and t5, t5, a7         # só é "1" se AMBOS terminaram

    beqz t5, MUSIC_LOOP_FIM

    # os dois terminaram -> reinicia tudo junto
    la  a0, CANAL_1
    call RESET_CANAL
    la  a0, CANAL_2
    call RESET_CANAL

MUSIC_LOOP_FIM:
    lw ra, 0(sp)
    addi sp, sp, 4
    ret

PLAY_MUSIC:
    addi sp, sp, -12
    sw ra, 0(sp)
    sw s0, 4(sp)
    sw s1, 8(sp)

    mv   s1, a0
    lw   t6, 12(s1)
    bnez t6, MUSIC_JA_ACABOU     # já tinha terminado -> só avisa e não faz nada

    lw   t0, 0(s1)
    lw   t3, 4(s1)
    lw   t4, 8(s1)

    beqz t3, TOCA_NOTA
    li a7,30
    ecall
    mv t2,a0              # guarda o tempo atual
    blt t2,t3,NAO_TERMINOU
    
    addi t0,t0,8
    sw   t0, 0(s1)
    bge  t0, t4, MARCA_FIM

TOCA_NOTA:
    lw   a0, 0(t0)
    lw   a1, 4(t0)
    li a7,31
    ecall
    
    add s0,t2,a1
    sw  s0,4(s1)

NAO_TERMINOU:
    li   a7, 0                    # sinaliza: ainda não terminou
    j    PLAY_MUSIC_RET

MARCA_FIM:
    li   t6, 1
    sw   t6, 12(s1)                # marca esse canal como terminado

MUSIC_JA_ACABOU:
    li   a7, 1                    # sinaliza: terminou (ou já tinha terminado)

PLAY_MUSIC_RET:
    lw   s1, 8(sp)
    lw   s0, 4(sp)
    lw   ra, 0(sp)
    addi sp, sp, 12
    ret

RESET_CANAL:
    mv   t0, a0

    lw   t1, 16(t0)         # endereço inicial da música
    sw   t1, 0(t0)          # reinicia ponteiro

    sw   zero, 4(t0)        # zera timer
    sw   zero, 12(t0)       # zera flag de "terminou"

    ret
